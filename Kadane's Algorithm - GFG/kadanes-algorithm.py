class Solution:
    ##Complete this function
    #Function to find the sum of contiguous subarray with maximum sum.
    def maxSubArraySum(self,arr,N):
        ##Your code here
        sumv = 0
        maxv = float("-inf")
        for i in range(0,N):
            sumv += arr[i]
            maxv = max(maxv,sumv)
            if sumv < 0:
                sumv = 0
        return maxv
            


